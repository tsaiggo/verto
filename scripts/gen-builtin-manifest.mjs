// Code generator for the built-in docs manifest.
//
// Scans `builtin-docs/` and emits `lib/content-source/builtin-manifest.generated.ts`,
// a plain TypeScript module that embeds every built-in doc (and the optional
// `navigation.json`) as a string. Embedding the corpus into the JS bundle means
// the built-in docs travel with the app and need no filesystem or network at
// runtime — important for the Tauri static-export build, which has no writable
// content folder.
//
// Run via `npm run gen:builtin` (also wired into `predev` / `prebuild`).

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, "..");
const docsRoot = path.join(repoRoot, "builtin-docs");
const outFile = path.join(
  repoRoot,
  "lib",
  "content-source",
  "builtin-manifest.generated.ts",
);

const INCLUDE = new Set([".md", ".mdx", ".json"]);

/** Recursively collect file paths (as segment arrays) under `dir`. */
async function collect(dir, rel, out) {
  let entries;
  try {
    entries = await fs.readdir(dir, { withFileTypes: true });
  } catch (err) {
    if (err && err.code === "ENOENT") return;
    throw err;
  }
  for (const entry of entries) {
    if (entry.name.startsWith(".")) continue;
    const abs = path.join(dir, entry.name);
    const segs = [...rel, entry.name];
    if (entry.isDirectory()) {
      await collect(abs, segs, out);
    } else if (
      entry.isFile() &&
      INCLUDE.has(path.extname(entry.name).toLowerCase())
    ) {
      out.push(segs);
    }
  }
}

async function main() {
  const paths = [];
  await collect(docsRoot, [], paths);
  paths.sort((a, b) => a.join("/").localeCompare(b.join("/")));

  const entries = [];
  for (const segs of paths) {
    const text = await fs.readFile(path.join(docsRoot, ...segs), "utf-8");
    entries.push({ path: segs, text });
  }

  const body = entries
    .map(
      (e) =>
        `  { path: ${JSON.stringify(e.path)}, text: ${JSON.stringify(e.text)} },`,
    )
    .join("\n");

  const out = `// AUTO-GENERATED by scripts/gen-builtin-manifest.mjs — do not edit by hand.
// Run \`npm run gen:builtin\` to regenerate after changing files in builtin-docs/.

import type { BuiltinManifestEntry } from "./builtin";

export const BUILTIN_MANIFEST: BuiltinManifestEntry[] = [
${body}
];
`;

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, out, "utf-8");
  console.log(
    `Wrote ${path.relative(repoRoot, outFile)} (${entries.length} entries).`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
